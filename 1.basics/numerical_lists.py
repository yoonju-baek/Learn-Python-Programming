# Numerical lists
# list() function - creates a list
# range() function - generate a series of numbers
# range(start_number, end_number)
# range(start_number, end_number, step) 
numbers = list(range(1,5))
print(numbers)

for value in range(1,5):
    print(value)

any_numbers = list(range(1,11,3))
print(any_numbers) 