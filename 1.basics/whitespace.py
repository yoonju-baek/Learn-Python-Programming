# Whitespace to Strings
# Tabs = \t
# Newlines = \n
print("Languages:\n\tPython\n\tJava\n\tJavascript\n\tC++")

# Stripping whitespace
# Eliminate right whitespace = rstrip()
# Eliminate left whitespace = lstrip()
# Eliminate both side whitespace = strip()
learning_language = ' python '
print("Eliminating whitespace right side:"+ learning_language.rstrip())
print("Eliminating whitespace left side:"+ learning_language.lstrip())
print("Eliminating whitespace both side:"+ learning_language.strip())
